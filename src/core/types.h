
#pragma once

#include <stdarg.h>
#include <stdbool.h>
#include <stdint.h>

// We use the following Rust-style acronyms for fixed-sized integers and floats
// For numbers where the exact size does not matter, a plain int/uint should be used instead.
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint32_t uint;
typedef uint64_t u64;
typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;
typedef float f32;
typedef double f64;
